user_word = input("Masukkan suatu kata: ")
user_word = user_word.upper()

for kata in user_word:
    if kata == "A":
        continue
    elif kata == "I":
        continue
    elif kata == "U":
        continue
    elif kata == "E":
        continue
    elif kata == "O":
        continue
    else:
        print(kata)
